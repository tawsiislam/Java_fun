public class Number {
	
	public static void main(String[] args)
	{	
	int numberOne; 
	int numberTwo;
	 
	numberOne = 5; 
	numberTwo = 10; 
	
	System.out.print(numberOne + " "); 
	System.out.println(numberTwo); 
	
	numberOne++; 
	System.out.println(numberOne);
	
	numberOne = numberOne + 2; 
	System.out.println(numberOne);
	
	numberOne = numberOne * numberTwo;
	System.out.println(numberOne);
	
	numberOne = numberOne / numberTwo; 
	System.out.println(numberOne);
	
	numberOne = numberOne % 3; //To find the rest after a division with 3
	System.out.println(numberOne);
	 
	numberOne += 3; //Add 3
	System.out.println(numberOne);
	
	numberOne -= 3; //Subtract 3
	System.out.println(numberOne);
	
	System.out.println(numberOne);
	
	
}
}
